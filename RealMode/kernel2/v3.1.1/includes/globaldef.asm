KERNEL_SEG16 equ 0x1000
KERNEL_INDEX16 equ 0x0000
KERNEL_BASE32 equ (KERNEL_SEG16*0x10 + KERNEL_INDEX16)

KERNEL_DISK_SECTOR equ 2
KERNEL_DISK_CYLINDER equ 0
KERNEL_DISK_HEAD equ 0
KERNEL_SIZE_SECTORS equ 16
KERNEL_SIZE_BYTES equ (KERNEL_SIZE_SECTORS*512)

STACK_SEG16 equ 0x2000
STACK_BASE32 equ (STACK_SEG16*0x10)
STACK_BP16 equ 0x7ffc
STACK_BP32 equ (STACK_BASE32 + STACK_BP16)

DATA0_SEG16 equ 0x2800
DATA0_BASE32 equ (DATA0_SEG16*0x10)

BOOTDEV_SEG16 equ 0x0000
BOOTDEV_INDEX16 equ 0x7bfe
BOOTDEV_BASE32 equ (BOOTDEV_SEG16*0x10 + BOOTDEV_INDEX16)

RETURN_MARGIN_CALLER equ 0x10
RETURN_MARGIN_CALLEE equ 0xc
TEXTBUF_SIZE equ 128

